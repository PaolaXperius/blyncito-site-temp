(function (BlincytoVideoPlayer, $, undefined) {
    "use strict";

    var player;
    var APIModules;
    var videoPlayer;
    var contentModule;
    var experienceModule;
    var playlistId = $('#PlaylistId').val();
    var videos = [];
    var currentVideo = 0;
    var calledFloodlight = false;
    var playlistItemTemplate = '<div class="playlist-item row collapse" id="VIDEO_ID">' +
                                   '<div class="video-player-icon small-2 medium-1 column"><img src="~/media/149DB68EF8D94D97AF6F682A7C918250"/></div>' +
                                   '<div class="video-title small-10 column">VIDEO_TITLE<sup>1</sup></div>' +
                                   '<div class="video-length small-10 medium-1 column">VIDEO_LENGTH</div>' +
                               '</div>';

    function toHHMMSS(milliseconds) {
        var sec_num = Math.round(parseInt(milliseconds, 10) / 1000);
        var hours = Math.floor(sec_num / 3600);
        var minutes = Math.floor((sec_num - (hours * 3600)) / 60);
        var seconds = sec_num - (hours * 3600) - (minutes * 60);
        if (hours == 0) { hours = ""; } else if(hours < 10) { hours = "0" + hours + ":"; }
        if (minutes < 10) { minutes = "0" + minutes; }
        if (seconds < 10) { seconds = "0" + seconds; }
        var time = hours + minutes + ':' + seconds;
        return time;
    }

    function buildPlaylistData () {
        // master array to hold retrieved playlist data
        contentModule.getPlaylistByID(playlistId, function (jsonData) {
            videos = jsonData.videos;
            var playlistContainer = $('.video-playlist');
            for(var i = 0; i < videos.length; i++){
                playlistContainer.append(playlistItemTemplate.replace('VIDEO_ID', videos[i].id).replace('VIDEO_TITLE', videos[i].displayName).replace('VIDEO_LENGTH', toHHMMSS(videos[i].length)));
            }
            $(".playlist-item").click(function () {
                videoPlayer.loadVideoByID(Number($(this).attr('id')));
            });
            videoPlayer.cueVideoByID(videos[currentVideo].id);
        })
    }

    BlincytoVideoPlayer.onMediaComplete = function (evt) {
        currentVideo++;
        if (currentVideo == videos.length) {
            currentVideo = 0;
        }
        videoPlayer.loadVideoByID(videos[currentVideo].id);
    };

    BlincytoVideoPlayer.onMediaBegin = function (evt) {
        if (!calledFloodlight) {
            callFloodlight('bphase2', 'blin45');
            calledFloodlight = true;
        }
        $(".playlist-item").removeClass('active');
        $('#' + evt.media.id).addClass('active');
    };

    BlincytoVideoPlayer.onTemplateLoad = function (experienceID) {
        // get a reference to the player and API Modules and Events
        player = brightcove.api.getExperience(experienceID);
        APIModules = brightcove.api.modules.APIModules;
    };

    BlincytoVideoPlayer.onTemplateReady = function (evt) {
        // get references to modules
        videoPlayer = player.getModule(APIModules.VIDEO_PLAYER);
        contentModule = player.getModule(APIModules.CONTENT);
        experienceModule = player.getModule(APIModules.EXPERIENCE);
        // fetch the playlist
        buildPlaylistData();

        videoPlayer.addEventListener(brightcove.api.events.MediaEvent.BEGIN, BlincytoVideoPlayer.onMediaBegin);
        videoPlayer.addEventListener(brightcove.api.events.MediaEvent.COMPLETE, BlincytoVideoPlayer.onMediaComplete);
    };

    $(window).on("resize orientationchange", function(){
        var resizeWidth = $(".video-player").width();
        var resizeHeight = $(".video-player").height();
        if (experienceModule && experienceModule.experience.type == "html") {
            experienceModule.setSize(resizeWidth, resizeHeight)
        }
    });

})(window.BlincytoVideoPlayer = window.BlincytoVideoPlayer || {}, jQuery);
